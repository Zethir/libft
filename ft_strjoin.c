#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char const *str;

	str = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (str == NULL)
		return (NULL);
	ft_memcpy((void *)str, (const void *)s1, ft_strlen(s1));
	ft_strcat((char *)str, s2);
	return ((char *)str);
}
